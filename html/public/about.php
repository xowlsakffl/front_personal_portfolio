<script>
    $(function(){
        win_info()
        get_offset()
        motion()

        function win_info(){
            scr_t=$(window).scrollTop()
            win_h=$(window).height()
        }
        function get_offset(){
            about_sec_2=$('.about_sec_2').offset().top
            about_sec_3=$('.about_sec_3').offset().top
            about_sec_5=$('.about_sec_5 .right_conf').offset().top
        }
        function motion(){

            if(scr_t >= about_sec_2 * 0.8){
				$('.about .menu_btn span').addClass('active')
			}else{
                $('.about .menu_btn span').removeClass('active')
            }

            parallax_y=(scr_t - about_sec_3)*0.6
			$('.about_sec_3').css('background-position','center '+parallax_y+'px')

            if(scr_t >= about_sec_5 - win_h * 0.5){
				$('.about_sec_5 .right_conf').addClass('active')
			}
        }

        $(window).scroll(function(){
            win_info()
            get_offset()
            motion()
        })
    })
</script>