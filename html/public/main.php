<script> 
    

    $(function(){
        $(window).on('load', function () {
            $('.left_line').css('height',$(window).height()*0.9+'px')
            $('.main_txt_box .top').css('animation','showTopText 1s 0.8s forwards')
            $('.main_txt_box .bottom').css('animation','showBottomText 0.5s 1.8s forwards')
            $('.main_image').addClass('active')
        })

        win_info()
        get_offset()
        motion()

        function win_info(){
            scr_t=$(window).scrollTop()
            win_h=$(window).height()
        }
        function get_offset(){
            offset_footer=$('.footer_wrap').offset().top
            slide_txt_1=$('.slide_txt_1').offset().top
            slide_txt_2=$('.slide_txt_2').offset().top
            sec2_title = $('.main_section_2 .title_line_box').offset().top
            sec4_title = $('.main_section_4 .title_line_box').offset().top
            sec5_title = $('.main_section_5 .title_line_box').offset().top
            sec6_title = $('.main_section_6 .title_line_box').offset().top
            banner = $('.main_section_3 .main_banner').offset().top
            sec4_cont = $('.main_section_4 .section_cont').offset().top
        }
        function motion(){
            if(scr_t < offset_footer - win_h){
                $('.left_line').css('height',scr_t+win_h * 0.9+'px')
            }
            
            wscroll = scr_t / 10;
            wscroll = 100 - wscroll;
            
			$('.slide_txt_1').css('transform','translateX('+wscroll+'%)')
            $('.slide_txt_2').css('transform','translateX('+-wscroll+'%)')

            if(scr_t >= sec2_title - win_h * 0.8){
				$('.main_section_2 .title_line').addClass('active')
                $('.main_section_2 .title_circle').addClass('active')
                $('.main_section_2 .title_txt').addClass('active')
			}

            $(".main_section_2 .box").each(function(){
                sec2_box = $(this).offset().top
                if(scr_t >= sec2_box - win_h * 0.7){
                    $(this).addClass("active")
                }
            })

            parallax_y=(scr_t - banner)*0.6
			$('.main_banner').css('background-position','center '+parallax_y+'px')

            if(scr_t >= sec4_title - win_h * 0.8){
				$('.main_section_4 .title_line').addClass('active')
                $('.main_section_4 .title_circle').addClass('active')
                $('.main_section_4 .title_txt').addClass('active')
			}

            if(scr_t >= sec4_cont - win_h * 0.6){
				$('.main_section_4 .section_cont').addClass('active')
			}

            if(scr_t >= sec5_title - win_h * 0.8){
				$('.main_section_5 .title_line').addClass('active')
                $('.main_section_5 .title_circle').addClass('active')
                $('.main_section_5 .title_txt').addClass('active')
                $('.main_section_5 .title_detail').addClass('active')
			}

            if(scr_t >= sec6_title - win_h * 0.8){
				$('.main_section_6 .title_line').addClass('active')
                $('.main_section_6 .title_circle').addClass('active')
                $('.main_section_6 .title_txt').addClass('active')
                $('.main_section_6 .title_detail').addClass('active')
			}

            $(".main_section_6 .submit_form>div").each(function(){
                sec6_form = $(this).offset().top
                if(scr_t >= sec6_form - win_h * 0.7){
                    $(this).addClass("active")
                }
            })

        }

        $(window).scroll(function(){
            win_info()
            get_offset()
            motion()
        })
        

        $('.form_btn button').click(function(){
            var formData = $("#inquiry").serialize();
            
            $.ajax({
                cache : false,
                url : "/public/inquiry/insert.php",
                type : 'POST', 
                data : formData, 
                dataType : 'json',
                success : function(data) {
                    if(data.code == "200"){
                        layer_popup("문의주셔서 감사합니다. 빠른 시일내에 답변드리겠습니다.");
                        /* mailTo(data.lan, data.name, data.email); */
                    }else{
                        layer_popup(data.msg);
                    }
                }, // success 
                error : function(){
                    alert('관리자에게 문의하세요.');
                }
            }); // $.ajax */
        })

        $("#contact_btn").click(function(){
            $(this).attr('data-click-state', 1);
            $(".splash").removeClass("active")
            $(".menu_btn").removeClass("active")
            $(".menu_btn button").text("MENU")
            $(".menu_logo").fadeOut()
            $(".menu").removeClass("active")
            var offset = $('#contact').offset();
            $('html').animate({scrollTop : offset.top}, 400);

        })

        function mailTo(lan, name, email){
            $.ajax({
                cache : false,
                url : "/_lib/mail.php",
                type : 'POST', 
                data : {lan : lan, name : name, email : email}, 
                success : function(data) {
                    window.location.href = '/';
                }, // success 
            }); // $.ajax */
        }
    })
</script>
